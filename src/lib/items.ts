export type ItemCategory =
  | "ball"
  | "heal"
  | "battle"
  | "evo"
  | "held"
  | "special"
  | "berry"
  | "tm"
  | "key";

export type ItemEffect =
  | { type: "heal_hp"; amount: number | "full" }
  | { type: "heal_status" }
  | { type: "revive"; hpPercent: number }
  | { type: "catch"; catchMultiplier: number }
  | {
      type: "evolve";
      targetPokemonId?: number;
      method?: "stone" | "trade" | "item";
    }
  | {
      type: "stat_boost";
      stat: keyof PokemonStats | "crit" | "acc" | "eva";
      amount: number;
    }
  | { type: "xp_boost"; multiplier: number }
  | { type: "mega_evo"; stoneId: string }
  | { type: "pp_restore"; amount: number | "full" }
  | { type: "friendship_boost" }
  | { type: "passive" } // held items handled entirely in engine
  | { type: "teach"; moveId: number; moveName: string };

export interface Item {
  id: string;
  name: string;
  spriteSlug: string;
  description: string;
  category: ItemCategory;
  effect: ItemEffect;
  buyable: boolean;
  lootable: boolean;
  shopPrice: number | null;
  condition?: (gameState: any) => boolean;
}

